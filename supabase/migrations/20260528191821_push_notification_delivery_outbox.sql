insert into public.device_tokens (user_id, token, platform, enabled, last_seen_at, updated_at)
select id, push_token, 'android', true, timezone('utc'::text, now()), timezone('utc'::text, now())
from public.users
where push_token is not null
  and length(trim(push_token)) > 0
on conflict (token) do update set
  user_id = excluded.user_id,
  enabled = true,
  last_seen_at = excluded.last_seen_at,
  updated_at = excluded.updated_at;

create or replace function private.enqueue_notification_deliveries()
returns trigger
language plpgsql
security definer
set search_path = public, private, pg_temp
as $$
begin
  insert into public.notification_deliveries (notification_id, recipient_id, device_token_id, token)
  select new.id, new.recipient_id, device_tokens.id, device_tokens.token
  from public.device_tokens
  where device_tokens.user_id = new.recipient_id
    and device_tokens.enabled is true
  on conflict (notification_id, device_token_id) do nothing;

  return new;
end;
$$;

drop trigger if exists notification_push_delivery_trigger on public.notifications;
create trigger notification_push_delivery_trigger
after insert on public.notifications
for each row execute function private.enqueue_notification_deliveries();
