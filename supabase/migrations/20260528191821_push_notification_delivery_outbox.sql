create or replace function private.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.users
    where users.id = auth.uid()
      and users.role = 'admin'
  );
$$;

drop policy if exists "Doctors are publicly readable" on public.doctors;
create policy "Doctors are publicly readable"
  on public.doctors
  for select
  to anon, authenticated
  using (true);

drop policy if exists "Authenticated doctors can create profile" on public.doctors;
create policy "Authenticated doctors can create profile"
  on public.doctors
  for insert
  to authenticated
  with check (owner_id = (select auth.uid()));

drop policy if exists "Users can update permitted doctors" on public.doctors;
create policy "Users can update permitted doctors"
  on public.doctors
  for update
  to authenticated
  using (
    private.is_admin()
    or owner_id = (select auth.uid())
    or id in (select users."doctorId" from public.users where users.id = (select auth.uid()))
  )
  with check (
    private.is_admin()
    or owner_id = (select auth.uid())
    or id in (select users."doctorId" from public.users where users.id = (select auth.uid()))
  );

drop policy if exists "Users can create own profile" on public.users;
create policy "Users can create own profile"
  on public.users
  for insert
  to authenticated
  with check (
    id = (select auth.uid())
    and coalesce(role, 'patient') = any (array['patient'::text, 'doctor'::text])
  );

drop policy if exists "Users can read permitted profiles" on public.users;
create policy "Users can read permitted profiles"
  on public.users
  for select
  to authenticated
  using (id = (select auth.uid()) or private.is_admin());

drop policy if exists "Users can update own profile" on public.users;
create policy "Users can update own profile"
  on public.users
  for update
  to authenticated
  using (id = (select auth.uid()))
  with check (
    id = (select auth.uid())
    and coalesce(role, 'patient') = any (array['patient'::text, 'doctor'::text])
  );

drop policy if exists "Users can read permitted appointments" on public.appointments;
create policy "Users can read permitted appointments"
  on public.appointments
  for select
  to authenticated
  using (
    private.is_admin()
    or patient_id = (select auth.uid())
    or doctor_id in (select users."doctorId" from public.users where users.id = (select auth.uid()))
  );

drop policy if exists "Users can update permitted appointment states" on public.appointments;
create policy "Users can update permitted appointment states"
  on public.appointments
  for update
  to authenticated
  using (
    private.is_admin()
    or (
      patient_id = (select auth.uid())
      and status = 'Accepted'
      and coalesce(payment_status, 'Unpaid') = any (array['Unpaid'::text, 'Rejected'::text])
    )
    or (
      status = 'Pending Approval'
      and doctor_id in (select users."doctorId" from public.users where users.id = (select auth.uid()))
    )
  )
  with check (
    private.is_admin()
    or (
      patient_id = (select auth.uid())
      and status = 'Accepted'
      and payment_status = any (array['Payment Submitted'::text, 'Pending Verification'::text])
      and payment_mode = any (array['UPI'::text, 'Cash'::text])
      and (payment_mode = 'Cash' or nullif(trim(transaction_id), '') is not null)
    )
    or (
      doctor_id in (select users."doctorId" from public.users where users.id = (select auth.uid()))
      and status = any (array['Accepted'::text, 'Cancelled'::text])
      and coalesce(payment_status, 'Unpaid') = 'Unpaid'
    )
  );

drop policy if exists "Users can read own notifications" on public.notifications;
create policy "Users can read own notifications"
  on public.notifications
  for select
  to authenticated
  using (recipient_id = (select auth.uid()));

drop policy if exists "Users can update own notifications" on public.notifications;
create policy "Users can update own notifications"
  on public.notifications
  for update
  to authenticated
  using (recipient_id = (select auth.uid()))
  with check (recipient_id = (select auth.uid()));

create or replace function private.create_notification(
  p_recipient_id uuid,
  p_actor_id uuid,
  p_appointment_id bigint,
  p_type text,
  p_title text,
  p_body text,
  p_metadata jsonb default '{}'::jsonb
)
returns void
language plpgsql
security definer
set search_path = public, private
as $$
begin
  if p_recipient_id is null then
    return;
  end if;

  insert into public.notifications (
    recipient_id,
    actor_id,
    appointment_id,
    type,
    title,
    body,
    metadata
  ) values (
    p_recipient_id,
    p_actor_id,
    p_appointment_id,
    p_type,
    p_title,
    p_body,
    coalesce(p_metadata, '{}'::jsonb)
  );
end;
$$;

create or replace function private.notify_admins(
  p_actor_id uuid,
  p_appointment_id bigint,
  p_type text,
  p_title text,
  p_body text,
  p_metadata jsonb default '{}'::jsonb
)
returns void
language plpgsql
security definer
set search_path = public, private
as $$
declare
  admin_row record;
begin
  for admin_row in select id from public.users where role = 'admin' loop
    perform private.create_notification(
      admin_row.id,
      p_actor_id,
      p_appointment_id,
      p_type,
      p_title,
      p_body,
      p_metadata
    );
  end loop;
end;
$$;

create or replace function private.handle_appointment_notifications()
returns trigger
language plpgsql
security definer
set search_path = public, private
as $$
declare
  doctor_user_id uuid;
  doctor_owner_id uuid;
  doctor_recipient uuid;
  payment_was_submitted boolean;
  payment_is_submitted boolean;
  metadata jsonb;
begin
  select users.id into doctor_user_id
  from public.users
  where users."doctorId" = new.doctor_id
  limit 1;

  select doctors.owner_id into doctor_owner_id
  from public.doctors
  where doctors.id = new.doctor_id
  limit 1;

  doctor_recipient := coalesce(doctor_user_id, doctor_owner_id);
  metadata := jsonb_build_object(
    'appointment_id', new.id,
    'patient_name', new.patient_name,
    'doctor_name', new.doctor_name,
    'amount', new.amount,
    'slot', new.slot,
    'appointment_date', new.appointment_date,
    'payment_status', new.payment_status,
    'status', new.status,
    'transaction_id', new.transaction_id,
    'payment_receiver_upi', new.payment_receiver_upi,
    'payout_status', new.payout_status,
    'doctor_payout_amount', new.doctor_payout_amount,
    'platform_fee_amount', new.platform_fee_amount
  );

  if tg_op = 'INSERT' then
    perform private.create_notification(
      doctor_recipient,
      new.patient_id,
      new.id,
      'booking_requested',
      'New booking request',
      coalesce(new.patient_name, 'A patient') || ' requested an appointment for ' || coalesce(new.slot, 'the selected slot') || '.',
      metadata
    );
    return new;
  end if;

  if tg_op = 'UPDATE' then
    if old.status is distinct from new.status and new.status = 'Accepted' then
      perform private.create_notification(
        new.patient_id,
        doctor_recipient,
        new.id,
        'booking_accepted',
        'Doctor accepted your booking',
        'Your doctor accepted the request. Pay by UPI and submit the UTR to confirm.',
        metadata
      );
    end if;

    if old.status is distinct from new.status and new.status = 'Cancelled' then
      perform private.create_notification(
        new.patient_id,
        doctor_recipient,
        new.id,
        'booking_declined',
        'Booking declined',
        'The doctor declined this request. You can book another slot or choose a different specialist.',
        metadata
      );
    end if;

    payment_was_submitted := coalesce(old.payment_status, 'Unpaid') in ('Payment Submitted', 'Pending Verification');
    payment_is_submitted := coalesce(new.payment_status, 'Unpaid') in ('Payment Submitted', 'Pending Verification');

    if not payment_was_submitted and payment_is_submitted then
      perform private.notify_admins(
        new.patient_id,
        new.id,
        'payment_submitted',
        'Payment proof submitted',
        coalesce(new.patient_name, 'A patient') || ' submitted ' || coalesce(new.payment_mode, 'payment') || ' proof' || case when new.transaction_id is not null then ' with UTR ' || new.transaction_id else '' end || '.',
        metadata
      );
      perform private.create_notification(
        doctor_recipient,
        new.patient_id,
        new.id,
        'payment_submitted',
        'Payment proof submitted',
        coalesce(new.patient_name, 'A patient') || ' submitted payment proof. Admin verification is pending.',
        metadata
      );
    end if;

    if old.payment_status is distinct from new.payment_status and new.payment_status = 'Verified' then
      perform private.create_notification(
        new.patient_id,
        new.payment_verified_by,
        new.id,
        'payment_verified',
        'Booking confirmed',
        'Admin verified your payment. Your appointment is confirmed.',
        metadata
      );
      perform private.create_notification(
        doctor_recipient,
        new.payment_verified_by,
        new.id,
        'payment_verified',
        'Payment verified',
        'Admin verified payment for ' || coalesce(new.patient_name, 'the patient') || '.',
        metadata
      );
    end if;

    if old.payment_status is distinct from new.payment_status and new.payment_status = 'Rejected' then
      perform private.create_notification(
        new.patient_id,
        new.payment_verified_by,
        new.id,
        'payment_rejected',
        'Payment proof needs correction',
        coalesce(new.payment_rejection_reason, 'Please recheck and submit the UTR again.'),
        metadata
      );
    end if;

    if old.payout_status is distinct from new.payout_status and new.payout_status = 'Paid' then
      perform private.create_notification(
        doctor_recipient,
        new.payout_marked_by,
        new.id,
        'payout_paid',
        'Payout marked paid',
        'Admin marked payout paid for appointment #' || new.id::text || '.',
        metadata || jsonb_build_object('doctor_payout_amount', new.doctor_payout_amount)
      );
    end if;
  end if;

  return new;
end;
$$;

